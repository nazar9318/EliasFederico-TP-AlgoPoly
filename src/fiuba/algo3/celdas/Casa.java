package fiuba.algo3.celdas;

public class Casa {
private int alquiler;
private int valor;
	public Casa() {
		
	}
	public int getAlquiler() {
		return alquiler;
	}
	public void setAlquiler(int alquiler) {
		this.alquiler = alquiler;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
}
